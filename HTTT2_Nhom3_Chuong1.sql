USE Student_Management_System;
GO

-- Bảng Department (Khoa)
CREATE TABLE Department (
    DepartmentID VARCHAR(8) PRIMARY KEY,     
    DepartmentName NVARCHAR(100)             
);

GO

-- Bảng Class (Lớp)
CREATE TABLE Class (
    ClassID VARCHAR(8) PRIMARY KEY,           
    ClassName NVARCHAR(100),                  
    ClassSize INT NULL,                       
    DepartmentID VARCHAR(8) FOREIGN KEY REFERENCES Department(DepartmentID)   
);
GO

-- Bảng Student (Sinh viên)
CREATE TABLE Student (
    StudentID VARCHAR(10) PRIMARY KEY,         
    StudentName NVARCHAR(100),                 
    ClassID VARCHAR(8) FOREIGN KEY REFERENCES Class(ClassID),   
    DepartmentID VARCHAR(8) FOREIGN KEY REFERENCES Department(DepartmentID),  
    DateOfBirth DATE,                          
    Gender NVARCHAR(10),                       
    Hometown NVARCHAR(100)                     
);
GO

-- Bảng Subject (Môn học)
CREATE TABLE [Subject] (
    SubjectID VARCHAR(8) PRIMARY KEY,           
    SubjectName NVARCHAR(100),                  
    Credits INT,                               
    Semester INT,                              
    SubjectDescription NVARCHAR(255)            
);

GO


-- Bảng Score (Điểm)
CREATE TABLE Score (
    StudentID VARCHAR(10) FOREIGN KEY REFERENCES Student(StudentID),   
    SubjectID VARCHAR(8) FOREIGN KEY REFERENCES [Subject](SubjectID),      
    ContinuousAssessmentScore FLOAT,                                   
    FinalExamScore FLOAT,                                              
    TotalScore FLOAT,                                                  
    LetterGrade NVARCHAR(2),                                           
    NumberOfAttempts INT,                                              
    [Status] VARCHAR(20) NULL,                                             
    PRIMARY KEY (StudentID, SubjectID)                                  
);

GO

-- Bảng TuitionFee (Học phí)
CREATE TABLE TuitionFee (
    StudentID VARCHAR(10) FOREIGN KEY REFERENCES Student(StudentID),   
    Semester INT,                                                      
    TuitionAmount FLOAT NULL                                            
);

GO

-- Bảng Award (Khen thưởng)
CREATE TABLE Award (
    AwardID VARCHAR(8) PRIMARY KEY,                                     
    StudentID VARCHAR(10) FOREIGN KEY REFERENCES Student(StudentID),   
    DecisionDate DATE,                                                  
    AwardAmount NVARCHAR(50),                                                                                           
    Reason NVARCHAR(255)                                                
);

GO

